  test "Mac with various hash algo with random keys" do
    data = "testing data for hmac"

    for d <- DigestContextBuilder.supported_digests() do
      res =
        case MacContextBuilder.mac_context(d) do
          {:error, res} ->
            {:error, res}

          ctx ->
            {:ok, res} =
              ctx
              |> Mac.mac_init()
              |> Mac.mac_update(data)
              |> Mac.mac_final()

            IO.inspect(res, limit: 10000)
            assert(not is_nil(MacResult.get_mac_result(res)))
            {key, nctx} = ContextConfig.get(MacResult.get_mac_context(res), :get_and_clear_key)
            IO.inspect(key, limit: 1000)
            IO.inspect(nctx, limit: 10000)
        end
    end
  end

