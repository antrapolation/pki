  test "output test" do
    data1 = "This is one data"
    data2 = "This is two data"

    for d <- DigestContextBuilder.supported_digests() do
      {:ok, res} =
        DigestContextBuilder.digest_context(d)
        |> Digest.digest_init()
        |> Digest.digest_update(data1)
        |> Digest.digest_final()

      {:ok, res2} =
        DigestContextBuilder.digest_context(d)
        |> Digest.digest_init()
        |> Digest.digest_update(data2)
        |> Digest.digest_final()

      assert DigestResult.get_digested_value(res) != DigestResult.get_digested_value(res2)
    end
  end

